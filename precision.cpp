int main()
{
    int n;
    long int l;
    cin>>n>>l;
    int pos[n];
    for(int j=0;j<n;j++)
    {
        cin>>pos[j];
    }
    sort(pos,pos+n);
/*
    for(int j=0;j<n;j++)
    {
        cout<<pos[j]<<" ";
    }
    cout<<endl;
*/

    double d2=0;
    int prev = 0;
    for(int i=0;i<n;i++)
    {
        double dis;
        //cout<<"prev:"<<prev<<" & current:"<<pos[i]<<endl;
        if(i==0)
        {
            dis = (double)(pos[i]-prev);
        }
        else
        {
            dis = (double)(pos[i]-prev)/2;
        }



        if(d2<=dis)
            d2=dis;

        //cout<<i<<"-"<<d2<<endl;

        prev = pos[i];
    }

    float dis = (double)(l-pos[n-1]);

    if(dis>d2)
        d2=dis;

    
    printf ("%0.9f", d2);

    return 0;

}
